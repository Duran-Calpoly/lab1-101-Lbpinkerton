def calculate_average(num1, num2, num3):
    """Calculate the mathematical average of three numbers."""
    return ((num1 + num2 + num3) / 3)


def add_tax(bill_total):
    """Return the total after adding a 10% sales tax."""
    return (bill_total * 1.10)


def greet_user(name):
    """Return a greeting that says 'Hello' followed by the user's name."""
    return (f'Hello {name}')